int CtoI( char * C )
{
	return (int)(*C) - (int)'0';
}

int ProductOfFiveChars( char * string )
{
	/* First get a pointer to our string that we can work with */
	char * t = string;

	/* Then obtain the first number (we can't start with zero now can we!) */
	unsigned int product = CtoI(t++);

	/* Commence productisation */
	while (t < string + 5)
		product *= CtoI(t++);

	return product;
}

int problem8( int argc, char ** argv )
{
  unsigned int max = 0;

  char * t = largenumber;
  char * end = largenumber;

  /* Find the end of the string */
  while (*end)
	  end++;

  /* Roll back 4 chars */
  end -= 4;

  while (t < end)
  {
	  unsigned int p = ProductOfFiveChars( t++ );
	  if (p > max)
		  max = p;
  }

  printf("The largest product of the 1000 numbers is %d\n", max);

  return 0;
}

